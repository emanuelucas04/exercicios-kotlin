/*
    Sintaxe: A sintaxe do Kotlin é um pouco diferente da do Java, embora as duas linguagens tenham muitas semelhanças.
    Por exemplo, a declaração de função no Kotlin começa com a palavra-chave fun, enquanto no Java começa com o tipo de retorno da função.

    Nulabilidade: Em Java, qualquer variável pode ser nula, a menos que seja declarada com o modificador final.
    Em Kotlin, por padrão, as variáveis não podem ser nulas a menos que sejam explicitamente declaradas como tal usando o operador ?.
    Isso ajuda a prevenir erros comuns em tempo de execução, como o NullPointerException.

    Interoperabilidade: Kotlin foi projetado para ser totalmente interoperável com Java,
    o que significa que você pode usar bibliotecas Java em projetos Kotlin e vice-versa.
    Isso é possível porque o Kotlin é executado na JVM (Java Virtual Machine), assim como o Java.

    Inferência de tipos: O Kotlin suporta a inferência de tipos,
    o que significa que o compilador pode determinar o tipo de uma variável com base no valor atribuído a ela.
    Isso pode tornar o código mais conciso e legível.

    Extensões: Uma das características únicas do Kotlin é a capacidade de adicionar funções de extensão a tipos existentes,
    incluindo tipos de terceiros. Isso pode tornar o código mais legível e modular,
    especialmente ao lidar com bibliotecas de terceiros.

 */